◊h1{Installation}

If these are not already on, please turn them on, otherwise you don't need to do anything.
◊ol{
    ◊li{◊a[#:href "https://download.racket-lang.org/"]{Download Racket https://download.racket-lang.org/}}
    ◊li{◊code{Edit->Preferences->Editing->General Editing->Show line numbers}}
    ◊li{◊code{View->Use Horizontal Layout} (optional but more space)}
    ◊li{In the Bottom Left Corner, Choose Language -> Beginning Student}
}

Optional:
◊ol{
    ◊li{For autocompletion: ◊code{File->Package Manager->drcomplete}}

    ◊li{ ◊code{Edit->Preferences->Editing->General Editing->Enable automatic parentheses, square brackets, and quotes} (it can lead to editing problems if you're not paying attention)}
}

