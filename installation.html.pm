◊h1{Installation}

◊ol{
    ◊li{◊a[#:href "https://download.racket-lang.org/"]{Download Racket https://download.racket-lang.org/}}
    ◊li{Edit->Preferences->Editing->General Editing->Show line numbers}
    ◊li{View->Use Horizontal Layout (optional but more space)}
    ◊li{In the Bottom Left Corner, Choose Language -> Beginning Student}
}

Optional:
◊ol{
    ◊li{For autocompletion: File->Package Manager->drcomplete}

    ◊li{(Introduced when we get to strings) Edit->Preferences->Editing->General Editing->Enable automatic parentheses, square brackets, and quotes}
}

